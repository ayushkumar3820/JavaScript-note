const alert = alert("Your message goes here.");
// The alert function shows a message to the user. It does not return a value.

const confirm = confirm("Your message goes here.");
// The confirm function shows a dialog with OK and Cancel buttons.
// If the user clicks OK, it returns true (Boolean value).
// If the user clicks Cancel, it returns false (Boolean value).

const prompt = prompt("Your message goes here.");
// The prompt function shows a dialog with a text input field.
// If the user enters a value and clicks OK, it returns that value (e.g., "ayush" if "ayush" is entered).
// If the user clicks Cancel, it returns null.
